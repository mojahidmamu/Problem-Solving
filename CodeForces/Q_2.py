# Question 2: Type Conversion

# Taking first number from user (as string)
num1 = input("Enter first number: ")

# Taking second number from user (as string)
num2 = input("Enter second number: ")

# Converting both inputs to integers
num1 = int(num1)
num2 = int(num2)

#  the sum of the two numbers
total = num1 + num2

# Printing the result
print("Sum:", total)
