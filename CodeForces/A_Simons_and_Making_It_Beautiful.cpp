int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        vector<int> a(n);
        for (int i = 0; i < n; i++)
        {
            cin >> a[i];
        }
        if (n > 1)
        {
            swap(a[0], a[n - 1]);
        }
        for (int x : a)
            cout << x << " ";

        cout << "\n";

}

return 0;
}
